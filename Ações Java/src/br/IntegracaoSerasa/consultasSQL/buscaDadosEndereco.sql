SELECT PAR.CODPARC,
       ENC.CODEND,
       ENC.NOMEEND,
       CID.CODCID,
       CID.NOMECID,
       BAI.NOMEBAI,
       UFS.UF,
       PAR.CEP,
       PAR.NUMEND
  FROM TGFPAR PAR
 INNER JOIN TSICID CID
    ON CID.CODCID = PAR.CODCID
 INNER JOIN TSIBAI BAI
    ON BAI.CODBAI = PAR.CODBAI
 INNER JOIN TSIUFS UFS
    ON UFS.CODUF = CID.UF
 INNER JOIN TSIEND ENC
    ON ENC.CODEND = PAR.CODEND
 WHERE PAR.CODPARC = :P_CODPARC